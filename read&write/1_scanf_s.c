#include<stdio.h> // 包含标准输入输出库

int main(void)
{
    char age; // 定义一个 char 变量用于存储输入的字符
    // 使用 scanf_s 安全地从标准输入读取格式化数据
    // scanf_s 函数：
    // 参数1: 格式字符串
    // 后续参数: 存储读取数据的变量的地址，对于字符和字符串，需要额外提供缓冲区大小
    // 返回值: 成功匹配并赋值的输入项的数量，如果在任何转换发生之前到达文件末尾或发生读取错误，则返回 EOF
    // 注意：对于 %c，scanf_s 需要一个额外的参数来指定缓冲区大小，即使是单个字符。
    scanf_s("%c",&age,1); // 从标准输入读取一个字符，并指定缓冲区大小为 1
    printf("age=%c\n",age); // 打印读取到的字符
    return 0; // 返回成功状态码
}